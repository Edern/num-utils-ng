#endif
#include "utils.h"
#include <ctype.h>

void skipWord(FILE * stream)
{
	char c;
	do {
		if ((c = fgetc(stream)) == EOF)
			perror(PACKAGE_NAME);
	} while (!isdigit(c) && !isspace(c) && (c != '.') && (c != '-'));
}

